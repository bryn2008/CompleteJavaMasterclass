package uk.co.cjmc.section5;

public class ExpressionsChallenge {

    public static void main(String[] args) {

        int score = 100; // score = 100 is an expression
        if (score > 99) { // if and inside brackets are expression
            System.out.println("You got the high score!"); // inside the quotes is an expression
            score = 0; // all of this is an expression
        }
    }
}
